    public class BoolToVisibilityConverter : IValueConverter
    {
        public object Convert(object value, Type targetType, object parameter, CultureInfo culture)
        {
            bool boolValue = (bool)value;
            if (boolValue)
            {
                return Visibility.Visible;
            }
            else
            {
                return Visibility.Collapsed;
            }
        }

        public object ConvertBack(object value, Type targetType, object parameter, CultureInfo culture)
        {
            throw new NotImplementedException();
        }
    }

    /// <summary>
    /// Interaction logic for DataGroupWindow.xaml
    /// </summary>
    public partial class DataGroupWindow : Window
    {
        CollectionView? myView = null;
        DataGroupViewModel viewModel;

        public DataGroupWindow()
        {
            InitializeComponent();
            viewModel = new DataGroupViewModel();
            this.DataContext = viewModel;
        }

        private void AddGrouping(object sender, RoutedEventArgs e)
        {
            myView = (CollectionView)CollectionViewSource.GetDefaultView(dg_GroupSample.ItemsSource);
            if (myView.CanGroup == true)
            {
                PropertyGroupDescription groupDescription
                    = new PropertyGroupDescription(nameof(DataGroupModel.GroupName));
                myView.GroupDescriptions.Add(groupDescription);

                dgCol_ResultValue.Visibility = Visibility.Hidden;
            }
            else
            {
                return;
            }
        }

        private void RemoveGrouping(object sender, RoutedEventArgs e)
        {
            myView = (CollectionView)CollectionViewSource.GetDefaultView(dg_GroupSample.ItemsSource);
            myView.GroupDescriptions.Clear();
            dgCol_ResultValue.Visibility = Visibility.Visible;
        }

